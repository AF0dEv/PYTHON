# OPERADORES ARITMÉTICOS

a = 5
b = 3
print(a + b) # Adição
print(a - b) # Subtração
print(a * b) # Multiplicação
print(a / b) # Divisão
print(a // b) # Divisão Inteira
print(a % b) # Resto de Divisão
print(a ** b) # Potência

# OPERADORES POR ATRIBUIÇÃO

c = 5
c = c + 2 # IGUAL ao SEGUINTE
c += 2 # IGUAL ao ANTERIOR
c *= 2 # Serve para "/=" e "-="
print(c)

# PRECEDÊNCIA

d = 10 + (3 * 2) ** 2 # Primeiro Resolve PARÊNTESIS, depois POTENCIAS, depois Muitliplicaçao/Divisão, por Ultimo SOMA/SUBTRAÇÃO
print(d)
# 1º -> PARÊNTESIS
# 2º -> POTÊNCIAS
# 3º -> MULTIPLICAÇAO / DIVISÃO
# 4º -> SOMA / SUBTRAÇÃO

